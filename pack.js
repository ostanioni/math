const { exec } = require('child_process');

// WEBPACK
exec(
  'npm i -D webpack webpack-bundle-analyzer webpack-cli webpack-dev-server webpack-merge',
  (error, stdout, stderr) => {
    if (error) {
      console.error(`exec error: ${error}`);
      return;
    }
    console.log(`stdout: ${stdout}`);
    console.error(`stderr: ${stderr}`);
  },
);
// HTML
